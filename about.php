<center><img src="images/about.jpg"></center><br>
 <p>Amdavad is well known among ancient cities of India. Ahmed Shah, ruler of Gujarat in those days established this city in 1411, hence it was named as Ahmedabad, and with the passage of time it became famous as Amdavad. There is a saying about the birth of this city: 
</p>
<p>“Jab kutte par Sassa aayaa, Tab Badshah ne shahar basaayaa”.
</p>
<p>(When the rabbit attacked the dog, the Sultan populated the city).
</p>
<p align="justify">This saying indicates something rather unique, as compared to the general events. The sultan must have visualized something special on the land of this place, therefore he selected it for establishing a city. Many peculiarities in civilization, sculpture, architecture, trade & commerce and industrial development are class apart even today. Economy and practical dexterity have grown with the life of a man from Amdavad. There is an unique personality in the resident of Amdavad, staying in remote foreign countries or in various other states of India; and they can be marked different. The father of Nation Mahatma Gandhi had started the movement of freedom, keeping in view Independence of India, and its functional centre was Amdavad. Ashrams st Kochrab and at Sabarmati were the main centre of the Non-co-operation movement Strongly attached to truth and non-violence, Respected BAPU had published two periodicals – NAVJIVAN (Gujarati) and YOUNG-INDIA (English) and that had spread the values throughout India. Gujarat Vidyapith has been founded here in order to establish the education system according to Indian culture and to abandon the British education, that nurtured the mentality of slaves. Great personalities such as Vinoba Bhave and Kaka Kalelkar had stayed at Sabarmati Ashram and Vidhapith here in Amdavad, for in many years. Jahangir, the moghul ruler at Delhi, had situated at Shahibaug near the bank of Sabarmati river, reigned as the commander of Gujarat for some time. The Sardar-Memorial, old secretariat building has been constructed in those days. Sir Thomas Row, a Dutch representative, who wanted the trade licence from moghul emperor (just like the British did), also resided here at PHUVARA in a big mansion named VALANDA – HAVELI (Residence of the Dutch) but that building is ruined at present. Sardar Vallabhbhai Patel, the Iron-man of India, was practising his career as lawyer and latter he served in the municipality, too. Amdavad was famous for textile, brocade and indigo industries in old days. The craftsmanslip was world famous, and brocade was highly demanded in foreign countries. Rai Bahadur Ranchhodlal Chhotalal had started the first Textile-mill at Amdavad in 1859, thereafter the textile industry developed to such an extent that Amdavad was known as Manchester of India. Small industries producing equipments and spare parts used in textiles also flourished here, and the industrial development speeded up, as a result the textile research institute (ATIRA) was founded at Amdavad. </p>
